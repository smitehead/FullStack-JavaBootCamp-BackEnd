SELECT constraint_name, column_name
FROM user_cons_columns
WHERE table_name IN ('MEMBER', 'PRODUCT', 'AUCTION_RESULT')
AND column_name IN ('EMD_NO', 'TRADE_EMD_NO', 'DELIVERY_EMD_NO');

