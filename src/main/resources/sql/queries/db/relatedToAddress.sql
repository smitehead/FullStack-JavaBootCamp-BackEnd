ALTER TABLE MEMBER DROP COLUMN EMD_NO;
ALTER TABLE MEMBER ADD ADDR_ROAD  VARCHAR2(200);
ALTER TABLE MEMBER ADD ADDR_SHORT VARCHAR2(50);
-- ADDR_DETAIL 컬럼은 기존 것 그대로 유지

-- 2. PRODUCT 테이블
